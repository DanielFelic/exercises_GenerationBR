//FUNÇÕES COM RETORNO

//Função adição:
fun adi(num1: Double, num2: Double): Double{

    val result = num1 + num2

    return result
}

//Função subtração:
fun sub(num1: Double, num2: Double): Double{

    val result = num1 - num2

    return result
}

//Função multiplicação:
fun mul(num1: Double, num2: Double): Double{

    val result = num1 * num2

    return result
}

//Função divisão:
fun div(num1: Double, num2: Double): Double{

    val result = num1 / num2

    return result
}


//FUNÇÕES SEM RETORNO

//Função adição:
fun adi1(num1: Double, num2: Double) {

    println("$num1 + $num2 = ${num1+num2}")
}

//Função subtração:
fun sub1(num1: Double, num2: Double) {

    println("$num1 - $num2 = ${num1-num2}")
}

//Função multiplicação:
fun mul1(num1: Double, num2: Double) {

    println("$num1 * $num2 = ${num1*num2}")
}

//Função divisão:
fun div1(num1: Double, num2: Double) {

    println("$num1 / $num2 = ${num1/num2}")
}